fun main(args: Array<String>) {

    val num1 = 1000
    val num2 = 100
    var opt: Int

    opt = num1 + num2
    println("Addition: $opt")

    opt = num1 - num2
    println("Subtraction: $opt")

    opt = num1 * num2
    println("Multiplication: $opt")

    opt = num1 / num2
    println("Division: $opt")

    opt = num1 % num2
    println("Modulus: $opt")
}